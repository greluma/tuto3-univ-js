let a, b;
(a = 3), (b = 2);

// suma
let suma = a + b;
console.log(suma);

// resta
let resta = a - b;
console.log(resta);

//multiplicacion
let multip = a * b;
console.log(multip);

//division
let divis = a / b;
console.log(divis);

//modulo(residuo de la divis)
let modulo = b % a;
console.log(modulo);

//exponente
let expo = a ** b;
console.log(expo);

//incremento (dos variantes)
//pre-incremento (el operador ++ antes de la variable)
let preIncremento;
preIncremento = ++a;
console.log(a);
console.log(preIncremento);

//post-increnmento (el operador ++ despues de la variable)
let postIncremento;
postIncremento = a++;
console.log(a);
console.log(postIncremento);

//decremento (dos variantes)
// pre-decremento
let proDecremento;
proDecremento = --b;
console.log(b);
console.log(proDecremento);

// post-decremento
let postDecremento;
postDecremento = b--;
console.log(b);
console.log(postDecremento);
